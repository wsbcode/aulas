// Types principais
//-----------------------------------//

// Type Sting, define que sera recebido é um texto
let name: string = "William";

// Type Number , define que sera recebido um número
let age: number = 33;

// Type Boolean , define que sera recebido Verdadeiro/False ( true / false)
let active: boolean = true;

// Type any , 'O coring das tipagens', usado para aceitar qualquer tipo que seja passado
let misty: any = ["William", 33, true];

//-----------------------------------//
// Type em Array
//-----------------------------------//

//Type String
let frutas: string[] = ["Uva", "Melão", "Melancia", "Cajá", "Goiaba"];

//Type Number
let password: number[] = [11111, 22222, 3333333, 444444, 55555];

//Type Boolean
let status: boolean[] = [true, false, true, false];
