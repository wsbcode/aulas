// LAÇO DE REPETIÇÃO COM (for)

// O for tradicional é dividido em 3 partes separadas por ";"
// 1. Início: Onde o contador começa (let inicio = 0)
// 2. Condição: Até quando ele deve rodar (enquanto for menor que 10)
// 3. Passo: O que acontece no fim de cada volta (soma +1 no contador)

for (let inicio = 0; inicio < 10; inicio++) {
   // Usamos Template Strings (``) para colocar a variável direto no texto
   console.log(`repetindo ${inicio} ate o o fim da condição`);
}
