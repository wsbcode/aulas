// Síncrono (Fila de espera)
// Executa uma tarefa por vez, em ordem.
// A próxima só começa quando a atual termina.
// Ponto chave: Bloqueia a execução se a tarefa for demorada.

//Exemplo de Sincronismo
// Nesse exemplo mostra um código Síncrono ,uma linha sendo executada por vez

let nome = "William";
let sobre = "Barbosa";
let idade = 33;

console.log(`Nome completo: ${nome} ${sobre} - Idade: ${idade}`);
